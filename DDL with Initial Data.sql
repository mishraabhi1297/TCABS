/* CREATE IDENTITY CLAIM LIST TABLE */

CREATE TABLE IDENTITYCLAIMLIST
(
	Id INT IDENTITY(1,1) PRIMARY KEY,
	ClaimType VARCHAR(256),
	ClaimValue VARCHAR(256),
	DisplayName VARCHAR(256)
);

/* CREATE IDENTITY USER TABLE */

CREATE TABLE IDENTITYUSER
(
	Id INT IDENTITY(1,1) PRIMARY KEY,
	Username VARCHAR(256),
	Email VARCHAR(256),
	EmailConfirmed BIT,
	PasswordHash VARCHAR(MAX),
	SecurityStamp VARCHAR(38),
	PhoneNumber VARCHAR(50),
	PhoneNumberConfirmed BIT,
	TwoFactorEnabled BIT,
	LockoutEnd DATETIME,
	LockoutEnabled BIT,
	AccessFailedCount BIT
);

/* CREATE IDENTITY LOGIN TABLE */

CREATE TABLE IDENTITYLOGIN
(
	LoginProvider VARCHAR(128),
	ProviderKey VARCHAR(128),
	Id INT FOREIGN KEY REFERENCES IDENTITYUSER,
	Name VARCHAR(256),
	PRIMARY KEY(LoginProvider, ProviderKey)
);

/* CREATE IDENTITY USER CLAIM TABLE */

CREATE TABLE IDENTITYUSERCLAIM
(
	Id INT IDENTITY(1,1) PRIMARY KEY,
	UserId INT FOREIGN KEY REFERENCES IDENTITYUSER,
	ClaimType VARCHAR(256),
	ClaimValue VARCHAR(256)
);

/* CREATE IDENTITY ROLE TABLE */

CREATE TABLE IDENTITYROLE
(
	Id INT IDENTITY(1,1) PRIMARY KEY,
	Name VARCHAR(50)
);

/* CREATE IDENTITY USER ROLE TABLE */

CREATE TABLE IDENTITYUSERROLE
(
	UserID INT FOREIGN KEY REFERENCES IDENTITYUSER,
	RoleId INT FOREIGN KEY REFERENCES IDENTITYROLE
);

/* CREATE IDENTITY ROLE CLAIM TABLE */

CREATE TABLE IDENTITYROLECLAIM
(
	Id INT IDENTITY(1,1) PRIMARY KEY,
	RoleId INT FOREIGN KEY REFERENCES IDENTITYROLE,
	ClaimType VARCHAR(256),
	ClaimValue VARCHAR(256)
);

/* CREATE UNIT TABLE */

CREATE TABLE UNIT
(
	UNIT_ID INT IDENTITY(1,1) PRIMARY KEY,
	UNIT_CODE VARCHAR(20),
	UNIT_NAME VARCHAR(50) NOT NULL
);

/* CREATE STUDY PERIOD TABLE */

CREATE TABLE STUDYPERIOD
(
	STUDYPERIOD_ID INT IDENTITY(1,1) PRIMARY KEY,
	STUDYPERIOD_NAME VARCHAR(50) NOT NULL
);

/* CREATE UNIT OFFERING TABLE */

CREATE TABLE UNITOFFERING
(
	UNITOFFERING_ID INT IDENTITY(1,1) PRIMARY KEY,
	UNIT_ID INT FOREIGN KEY REFERENCES UNIT,
	STUDYPERIOD_ID INT FOREIGN KEY REFERENCES STUDYPERIOD,
	Id INT FOREIGN KEY REFERENCES IDENTITYUSER,
	UNITOFFERING_STARTDATE DATE NOT NULL
);

/* CREATE ENROLMENT TABLE */

CREATE TABLE ENROLMENT
(
	ENROLMENT_ID INT IDENTITY(1,1) PRIMARY KEY,
	Id INT FOREIGN KEY REFERENCES IDENTITYUSER,
	UNITOFFERING_ID INT FOREIGN KEY REFERENCES UNITOFFERING
);

/* CREATE PROJECT ROLE GROUP TABLE */

CREATE TABLE PROJECTROLEGROUP
(
	PROJECTROLEGROUP_ID INT IDENTITY(1,1) PRIMARY KEY,
	PROJECTROLEGROUP_TYPE VARCHAR(50) NOT NULL
);

/* CREATE PROJECT TABLE */

CREATE TABLE PROJECT
(
	PROJECT_ID INT IDENTITY(1,1) PRIMARY KEY,
	PROJECTROLEGROUP_ID INT FOREIGN KEY REFERENCES PROJECTROLEGROUP,
	PROJECT_NAME VARCHAR(50) NOT NULL
);

/* CREATE PROJECT ALLOC TABLE */

CREATE TABLE PROJECTALLOC
(
	PROJECTALLOC_ID INT IDENTITY(1,1) PRIMARY KEY,
	UNITOFFERING_ID INT FOREIGN KEY REFERENCES UNITOFFERING,
	PROJECT_ID INT FOREIGN KEY REFERENCES PROJECT,
	PROJECT_BUDGET INT
);

/* CREATE TEAM TABLE */

CREATE TABLE TEAM
(
	TEAM_ID INT IDENTITY(1,1) PRIMARY KEY,
	Id INT FOREIGN KEY REFERENCES IDENTITYUSER,
	TEAM_NAME VARCHAR(50) NOT NULL
);

/* CREATE PROJECT RECORD TABLE */

CREATE TABLE PROJECTRECORD
(
	PROJECTRECORD_ID INT IDENTITY(1,1) PRIMARY KEY,
	TEAM_ID INT FOREIGN KEY REFERENCES TEAM,
	PROJECTALLOC_ID INT FOREIGN KEY REFERENCES PROJECTALLOC,
	PROJECTRECORD_ISCURRENT BIT
);

/* CREATE TEAM ALLOC TABLE */

CREATE TABLE TEAMALLOC
(
	TEAMALLOC_ID INT IDENTITY(1,1) PRIMARY KEY,
	TEAM_ID INT FOREIGN KEY REFERENCES TEAM,
	ENROLMENT_ID INT FOREIGN KEY REFERENCES ENROLMENT,
	PROJECTRECORD_ID INT FOREIGN KEY REFERENCES PROJECTRECORD,
	TEAMALLOC_PROJECTMANAGER BIT, 
	TEAMALLOC_ISINTEAM BIT
);

/* CREATE PROJECT ROLE TABLE */

CREATE TABLE PROJECTROLE
(
	PROJECTROLE_ID INT IDENTITY(1,1) PRIMARY KEY,
	PROJECTROLE_NAME VARCHAR(50) NOT NULL,
	PROJECTROLE_WAGE INT
);

/* CREATE PROJECT ROLE GROUP ALLOC TABLE */

CREATE TABLE PROJECTROLEGROUPALLOC
(
	PROJECTROLEGROUPALLOC_ID INT IDENTITY(1,1) PRIMARY KEY,
	PROJECTROLEGROUP_ID INT FOREIGN KEY REFERENCES PROJECTROLEGROUP,
	PROJECTROLE_ID INT FOREIGN KEY REFERENCES PROJECTROLE 
);

/* CREATE TIME LOG TABLE */

CREATE TABLE TIMELOG
(
	TIMELOG_ID INT IDENTITY(1,1) PRIMARY KEY,
	TEAMALLOC_ID INT FOREIGN KEY REFERENCES TEAMALLOC,
	PROJECTROLEGROUPALLOC_ID INT FOREIGN KEY REFERENCES PROJECTROLEGROUPALLOC,
	TIMELOG_TASKNAME VARCHAR(50) NOT NULL,
	TIMELOG_STARTTIME DATETIME NOT NULL,
	TIMELOG_ENDTIME DATETIME NOT NULL,
	TIMELOG_CURRENTTIME DATETIME NOT NULL,
	TIMELOG_STATUS VARCHAR(20) NOT NULL,
	TIMELOG_TOTALSALARY INT 
);

